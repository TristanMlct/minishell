#include "libft.h"

void	ft_strarrdel(char ***arr)
{
	int	i;

	i = -1;
	if (!arr || !*arr)
		return ;
	while ((*arr)[++i])
		free((*arr)[i]);
	free(*arr);
	*arr = NULL;
}
